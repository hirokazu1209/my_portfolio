class HomesController < ApplicationController
  before_action :authenticate_user!

  def top
    @user = current_user
  end

  def edit
    @user = current_user
  end

  def update
    @user = current_user
    if @user.update(user_params)
      redirect_to root_path
    else
      render :edit
    end
  end

  private

  def user_params
    params.require(:user).permit(:name, :email, :self_introduction, :profile_picture_pass)
  end
end
